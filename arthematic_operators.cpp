#include <iostream>

int main(){
    int a = 10;
    int b = 3;

    std::cout << "a + b = " << a + b << std::endl;    // 13
    std::cout << "a - b = " << a - b << std::endl;    // 7
    std::cout << "a * b = " << a * b << std::endl;    // 30
    std::cout << "a / b = " << a / b << std::endl;    // 3 (integer division)
    std::cout << "a % b = " << a % b << std::endl;    // 1
    
}